#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*buf;

	if (!s)
		return (NULL);
	buf = ft_strnew(len);
	if (!buf)
		return (NULL);
	ft_strncpy(buf, &s[start], len);
	return (buf);
}
